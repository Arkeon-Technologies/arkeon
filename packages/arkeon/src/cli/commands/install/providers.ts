const gemini = makeSkillDirProvider("gemini", "Gemini CLI (skills in ~/.gemini/skills/)", ".gemini");

// ---------------------------------------------------------------------------
// Cursor — .cursor/commands/<name>.md (project-local, flat files)
// ---------------------------------------------------------------------------

const cursor: Provider = {
  name: "cursor",
  description: "Cursor (commands in .cursor/commands/)",
  skillDir: () => join(process.cwd(), ".cursor", "commands"),
  skillFileName: ".md",
  isGlobal: false,

  skillNames() {
    const skills = SKILLS?.["cursor"];
    return skills ? Object.keys(skills) : [];
  },

  install() {
    const dir = this.skillDir();
    const skills = SKILLS?.["cursor"] ?? {};
    const installed: string[] = [];
    const currentNames = new Set(Object.keys(skills));

    // Remove stale arkeon-* commands from previous versions
    try {
      for (const entry of readdirSync(dir, { withFileTypes: true })) {
        if (entry.isFile() && entry.name.startsWith("arkeon-") && entry.name.endsWith(".md")) {
          const base = entry.name.replace(/\.md$/, "");
          if (!currentNames.has(base)) {
            rmSync(join(dir, entry.name));
          }
        }
      }
    } catch {
      // dir may not exist yet
    }

    for (const [skillName, content] of Object.entries(skills) as [string, string][]) {
      if (!VALID_NAME.test(skillName)) continue;
      mkdirSync(dir, { recursive: true });
      writeFileSync(join(dir, `${skillName}.md`), content);
      installed.push(skillName);
    }

    return { installed, dir };
  },

  uninstall() {
    const dir = this.skillDir();
    const removed: string[] = [];

    for (const skillName of this.skillNames()) {
      const filePath = join(dir, `${skillName}.md`);
      if (existsSync(filePath)) {
        rmSync(filePath);
        removed.push(skillName);
      }
    }

    return { removed };
  },
};

// ---------------------------------------------------------------------------
// AGENTS.md — universal context file written to project root
// ---------------------------------------------------------------------------

const AGENTS_MD_MARKER = "<!-- arkeon:managed";

/** Write AGENTS.md to the current project root. */
export function installAgentsMd(): boolean {
  if (!AGENTS_MD) return false;
  const dest = join(process.cwd(), "AGENTS.md");
  writeFileSync(dest, AGENTS_MD);
  return true;
}

/** Remove AGENTS.md from the current project root if it's arkeon-managed. */
export function uninstallAgentsMd(): boolean {
  const dest = join(process.cwd(), "AGENTS.md");
  if (!existsSync(dest)) return false;
  try {
    const content = readFileSync(dest, "utf-8");
    if (!content.includes(AGENTS_MD_MARKER)) return false;
    rmSync(dest);
    return true;
  } catch {
    return false;
  }
}

/** Check if an arkeon-managed AGENTS.md exists in cwd. */
export function hasArkeonAgentsMd(): boolean {
  const dest = join(process.cwd(), "AGENTS.md");
  if (!existsSync(dest)) return false;
  try {
    const content = readFileSync(dest, "utf-8");
    return content.includes(AGENTS_MD_MARKER);
  } catch {
    return false;
  }
}

// ---------------------------------------------------------------------------
// Agents — meta-provider that installs everything
// ---------------------------------------------------------------------------

/** Individual providers (not including the "agents" meta-provider). */
export const individualProviders: Provider[] = [claude, codex, cursor, gemini];

const agents: Provider = {
  name: "agents",
  description: "All AI coding tools (Claude, Codex, Cursor, Gemini + AGENTS.md)",
  skillDir: () => "",
  skillFileName: "",
  isGlobal: false,

  skillNames() {
    return [...new Set(individualProviders.flatMap((p) => p.skillNames()))];
  },

  install() {
    const allInstalled: string[] = [];
    const dirs: string[] = [];

    for (const provider of individualProviders) {
      try {
        const { installed, dir } = provider.install();
        allInstalled.push(...installed.map((s) => `${provider.name}/${s}`));
        if (dir) dirs.push(dir);
      } catch (err) {
        output.progress(`Warning: failed to install ${provider.name} skills: ${(err as Error).message}`);
      }
    }

    // Write AGENTS.md
    if (installAgentsMd()) {
      allInstalled.push("AGENTS.md");
      dirs.push(process.cwd());
    }

    return { installed: allInstalled, dir: dirs.join(", ") };
  },

  uninstall() {
    const allRemoved: string[] = [];

    for (const provider of individualProviders) {
      try {
        const { removed } = provider.uninstall();
        allRemoved.push(...removed.map((s) => `${provider.name}/${s}`));
      } catch {
        // best-effort
      }
    }

    if (uninstallAgentsMd()) {
      allRemoved.push("AGENTS.md");
    }

    return { removed: allRemoved };
  },
};

// ---------------------------------------------------------------------------
// Registry
// ---------------------------------------------------------------------------

export const providers: Record<string, Provider> = {
  agents,
  claude,
  codex,
  cursor,
  gemini,
};

export function getProvider(name: string): Provider | null {
  return Object.hasOwn(providers, name) ? providers[name]! : null;
}

export function listProviders(): Provider[] {
  return Object.values(providers);
}
