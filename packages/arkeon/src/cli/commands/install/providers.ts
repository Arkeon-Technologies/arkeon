/**
 * Provider registry for skill installation.
 *
 * A "provider" is a target AI coding environment. Each provider knows where
 * to write skills and what format to use.
 *
 * Providers are either global (skills in ~/.<tool>/skills/) or project-local
 * (skills in .cursor/commands/ relative to cwd). The "agents" meta-provider
 * installs all providers at once plus a universal AGENTS.md file.
 */

import { existsSync, mkdirSync, readFileSync, readdirSync, rmSync, writeFileSync } from "node:fs";
import { homedir } from "node:os";
import { join } from "node:path";

import { output } from "../../lib/output.js";

// Bundled skill content — generated by scripts/bundle-assets.ts
import { AGENTS_MD, SKILLS } from "../../../generated/assets.js";

export interface Provider {
  name: string;
  description: string;
  /** Where skills are written. Global providers return an absolute path; project-local ones return relative to cwd. */
  skillDir: () => string;
  skillFileName: string;
  /** True = installed to home dir (~/.tool/skills/). False = installed relative to cwd. */
  isGlobal: boolean;
  skillNames: () => string[];
  install: () => { installed: string[]; dir: string };
  uninstall: () => { removed: string[] };
}

const VALID_NAME = /^[a-z0-9][a-z0-9-]*$/;

// ---------------------------------------------------------------------------
// Helpers for skill-dir providers (Claude, Codex, Gemini)
// ---------------------------------------------------------------------------

/** Creates a provider that writes SKILL.md into ~/.<tool>/skills/<name>/ */
function makeSkillDirProvider(
  name: string,
  description: string,
  toolDir: string,
): Provider {
  return {
    name,
    description,
    skillDir: () => join(homedir(), toolDir, "skills"),
    skillFileName: "SKILL.md",
    isGlobal: true,

    skillNames() {
      const skills = SKILLS?.[name];
      return skills ? Object.keys(skills) : [];
    },

    install() {
      const dir = this.skillDir();
      const skills = SKILLS?.[name] ?? {};
      const installed: string[] = [];
      const currentNames = new Set(Object.keys(skills));

      // Remove stale arkeon-* skills from previous versions
      try {
        for (const entry of readdirSync(dir, { withFileTypes: true })) {
          if (entry.isDirectory() && entry.name.startsWith("arkeon-") && !currentNames.has(entry.name)) {
            rmSync(join(dir, entry.name), { recursive: true });
          }
        }
      } catch {
        // dir may not exist yet — that's fine
      }

      for (const [skillName, content] of Object.entries(skills) as [string, string][]) {
        if (!VALID_NAME.test(skillName)) continue;
        const skillDir = join(dir, skillName);
        mkdirSync(skillDir, { recursive: true });
        writeFileSync(join(skillDir, this.skillFileName), content);
        installed.push(skillName);
      }

      return { installed, dir };
    },

    uninstall() {
      const dir = this.skillDir();
      const removed: string[] = [];

      for (const skillName of this.skillNames()) {
        const skillDir = join(dir, skillName);
        if (existsSync(skillDir)) {
          rmSync(skillDir, { recursive: true });
          removed.push(skillName);
        }
      }

      return { removed };
    },
  };
}

